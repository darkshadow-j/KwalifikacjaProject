    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
        System.out.println("Zaloguj: " + loginTF.getText() + " / " + passwordTF.getText());
        HQL hql = new HQL();
        Uzytkownicy uzytkownik = hql.CheckUser(loginTF.getText(), passwordTF.getText());
        if (uzytkownik != null) {
            PageManager.CloseOpenLoginPage();
            PageManager.OpenSzukajOsobyPage();
        } else {
            JOptionPane.showMessageDialog(this,
                    "Nieprawidlowa nazwa uzytkownika lub haslo",
                    "BAD USER",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

